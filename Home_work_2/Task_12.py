# Решение задачи:
# Для решения данной задачи нужно найти два натуральных числа, 
# которые имеют заданную сумму S и заданное произведение P. 
# Можно перебирать все пары натуральных чисел и проверять их сумму 
# и произведение, но это будет не очень эффективно. 
# Вместо этого воспользуемся следующим методом.

# Рассмотрим квадратичное уравнение ax^2 + bx + c = 0, где a=1, b=−S и c=P.
# Если существует решение этого уравнения в целых числах, 
# то эти числа и будут искомыми числами X и Y.

# Можно решить квадратичное уравнение и проверить, 
# является ли корень целым числом. Если это так, 
# то это и будет X или Y (четверка решений уравнения будет иметь 
# перестановки корней, т.е. решение для X и Y 
# может быть определено в произвольном порядке). 
# Но существует более простой и эффективный способ.

# Обозначим D=b^2 - 4ac. Если D < 0, 
# то уравнение не имеет решений в целых числах. 
# Если D ≥ 0, то корни уравнения будут вещественными 
# и равны x=(−b±√D)/2a. Чтобы x был целым числом, 
# нужно, чтобы √D было целым числом. Иначе говоря, требуется, 
# чтобы D было квадратом целого числа. Если это выполнено, 
# то x будет целым числом.

# Таким образом, мы можем просто вычислить D и проверить, 
# является ли он квадратом целого числа. Если это так, 
# то мы нашли искомые числа X и Y.

# Вот, как это можно реализовать на языке Python:

S, P = map(int, input().split())
for x in range(1, S+1):
    if P % x != 0:
        continue
    y = S - x
    if x * y == P:
        print(x, y)
        break
else:
    print("No solution")

